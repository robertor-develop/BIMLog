      doc.fontSize(8).font(overdue ? "Helvetica-Bold" : "Helvetica")
        .fillColor(overdue ? "#DC2626" : "#111")
        .text(`${rfi.number} | ${rfi.subject} | ${rfi.status} | Age: ${ageDays}d${overdue ? " OVERDUE" : ""}`, { indent: 5 });
