function pdfHeader(doc: PDFKit.PDFDocument, project: { name: string; code: string }, title: string) {
  doc.fontSize(16).font("Helvetica-Bold").fillColor("#1D4ED8").text("BIMLog", 50, 45);
  doc.fontSize(10).font("Helvetica").fillColor("#000").text(`by IgniteSmart`, { continued: false });
  doc.moveDown(0.3);
  doc.fontSize(14).font("Helvetica-Bold").fillColor("#111").text(title, { align: "center" });
  doc.fontSize(10).font("Helvetica").fillColor("#666")
    .text(`Project: ${project.name} (${project.code})  |  Generated: ${new Date().toLocaleDateString()}`, { align: "center" });
  doc.moveDown();
  doc.moveTo(50, doc.y).lineTo(doc.page.width - 50, doc.y).strokeColor("#D1D5DB").stroke();
  doc.moveDown();
}

function pdfFooter(doc: PDFKit.PDFDocument) {
  doc.fontSize(7).font("Helvetica").fillColor("#9CA3AF")
    .text("Confidential — Generated by BIMLog by IgniteSmart. For authorized use only.", 50, doc.page.height - 40, { align: "center" });
}

function row(doc: PDFKit.PDFDocument, label: string, value: string, labelWidth = 160) {
  const y = doc.y;
  doc.fontSize(9).font("Helvetica-Bold").fillColor("#374151").text(label, 50, y, { width: labelWidth });
  doc.fontSize(9).font("Helvetica").fillColor("#111").text(value, 50 + labelWidth, y, { width: doc.page.width - 50 - labelWidth - 50 });
  doc.moveDown(0.3);
}

async function getProject(projectId: number) {
  const [p] = await db.select().from(projectsTable).where(eq(projectsTable.id, projectId));
  return p;
}

// ── PROJECT HEALTH ─────────────────────────────────────────────────────────────
router.get("/projects/:projectId/reports/project-health/pdf", async (req, res) => {
  const userId = await verifyReportToken(req, res);
  if (!userId) return;
  const projectId = Number(req.params.projectId);
  try {
    const project = await getProject(projectId);
    if (!project) { res.status(404).json({ error: "Project not found" }); return; }
    const [rfis, subs, files] = await Promise.all([
      db.select().from(rfisTable).where(eq(rfisTable.projectId, projectId)),
      db.select().from(submittalsTable).where(eq(submittalsTable.projectId, projectId)),
      db.select().from(filesTable).where(eq(filesTable.projectId, projectId)),
    ]);
    const now = Date.now();
    const openRfis = rfis.filter(r => r.status !== "closed");
    const overdueRfis = openRfis.filter(r => r.dueDate && new Date(r.dueDate).getTime() < now);
    const pendingSubs = subs.filter(s => ["pending", "under_review"].includes(s.status));
    const validFiles = files.filter(f => f.status === "valid");
    const compRate = files.length ? Math.round((validFiles.length / files.length) * 100) : 0;

    const doc = new PDFDocument({ size: "LETTER", margin: 50 });
    res.setHeader("Content-Type", "application/pdf");
    res.setHeader("Content-Disposition", `attachment; filename="project-health-${project.code}.pdf"`);
    doc.pipe(res);
    pdfHeader(doc, project, "Project Health Report");

    row(doc, "Project Name", project.name);
    row(doc, "Project Code", project.code);
    row(doc, "Status", project.status.toUpperCase());
    doc.moveDown(0.5);
    doc.fontSize(11).font("Helvetica-Bold").fillColor("#111").text("Summary Statistics");
    doc.moveDown(0.3);
    row(doc, "Total RFIs", String(rfis.length));
    row(doc, "Open RFIs", String(openRfis.length));
    row(doc, "Overdue RFIs", String(overdueRfis.length));
    row(doc, "Total Submittals", String(subs.length));
    row(doc, "Pending Submittals", String(pendingSubs.length));
    row(doc, "Total Files", String(files.length));
    row(doc, "Compliant Files", String(validFiles.length));
    row(doc, "Compliance Rate", `${compRate}%`);
    pdfFooter(doc);
    doc.end();
  } catch (err) {
    res.status(500).json({ error: err instanceof Error ? err.message : "Internal server error" });
  }
});

